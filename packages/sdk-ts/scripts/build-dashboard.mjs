#!/usr/bin/env node
/**
 * Builds the dashboard SPA via Vite, then serializes its output as a
 * TypeScript module at `src/handler/dashboard-bundle.ts`.
 *
 * The generated module is a build artifact (gitignored). It exposes:
 *   - DASHBOARD_INDEX_HTML: string  — the dashboard's index.html, with
 *     asset paths still relative (`./assets/...`). The HTTP handler
 *     rewrites these to mount-relative URLs at request time.
 *   - DASHBOARD_LOGIN_HTML: string  — same shell as INDEX (the SPA owns
 *     its own /login route via wouter).
 *   - DASHBOARD_ASSETS: Record<string, { content: string; contentType: string }>
 *     keyed by asset filename (e.g. `index-D_tPEKcV.js`). `content` is
 *     base64; the route handler decodes at response time.
 *
 * Exits non-zero if vite build fails — no silent fallbacks.
 */
import { spawnSync } from 'node:child_process'
import { promises as fs } from 'node:fs'
import { dirname, join, resolve } from 'node:path'
import { fileURLToPath } from 'node:url'

const __dirname = dirname(fileURLToPath(import.meta.url))
const SDK_ROOT = resolve(__dirname, '..')
const REPO_ROOT = resolve(SDK_ROOT, '..', '..')
const DASHBOARD_ROOT = resolve(REPO_ROOT, 'packages', 'dashboard')
const DASHBOARD_DIST = join(DASHBOARD_ROOT, 'dist')
const OUTPUT_FILE = join(SDK_ROOT, 'src', 'handler', 'dashboard-bundle.ts')

const CONTENT_TYPES = {
  '.js': 'application/javascript; charset=utf-8',
  '.mjs': 'application/javascript; charset=utf-8',
  '.css': 'text/css; charset=utf-8',
  '.map': 'application/json; charset=utf-8',
  '.json': 'application/json; charset=utf-8',
  '.svg': 'image/svg+xml',
  '.png': 'image/png',
  '.jpg': 'image/jpeg',
  '.jpeg': 'image/jpeg',
  '.gif': 'image/gif',
  '.webp': 'image/webp',
  '.ico': 'image/x-icon',
  '.woff': 'font/woff',
  '.woff2': 'font/woff2',
  '.ttf': 'font/ttf',
  '.otf': 'font/otf',
}

function contentTypeFor(name) {
  const dot = name.lastIndexOf('.')
  if (dot === -1) return 'application/octet-stream'
  return CONTENT_TYPES[name.slice(dot).toLowerCase()] ?? 'application/octet-stream'
}

function buildDashboard() {
  console.log('[build-dashboard] Running `pnpm --filter @artanis-ai/gravel-dashboard build`...')
  const result = spawnSync('pnpm', ['--filter', '@artanis-ai/gravel-dashboard', 'build'], {
    cwd: REPO_ROOT,
    stdio: 'inherit',
    env: process.env,
  })
  if (result.status !== 0) {
    console.error(`[build-dashboard] vite build failed (exit ${result.status})`)
    process.exit(result.status ?? 1)
  }
}

async function readDistTree(root) {
  const out = []
  async function walk(dir, relPrefix) {
    const entries = await fs.readdir(dir, { withFileTypes: true })
    for (const entry of entries) {
      const abs = join(dir, entry.name)
      const rel = relPrefix ? `${relPrefix}/${entry.name}` : entry.name
      if (entry.isDirectory()) {
        await walk(abs, rel)
      } else if (entry.isFile()) {
        out.push({ rel, abs })
      }
    }
  }
  await walk(root, '')
  return out
}

async function main() {
  buildDashboard()

  // Sanity: the dist must exist after the build.
  const indexHtmlPath = join(DASHBOARD_DIST, 'index.html')
  try {
    await fs.access(indexHtmlPath)
  } catch {
    console.error(`[build-dashboard] expected ${indexHtmlPath} after build, not found`)
    process.exit(1)
  }

  const indexHtml = await fs.readFile(indexHtmlPath, 'utf8')

  // Walk the assets tree. We key assets by their basename because the
  // handler exposes them under a flat `/_assets/:filename` route.
  const assetsDir = join(DASHBOARD_DIST, 'assets')
  let assetFiles = []
  try {
    assetFiles = await readDistTree(assetsDir)
  } catch (err) {
    if (err.code !== 'ENOENT') throw err
  }

  const assets = {}
  for (const { rel, abs } of assetFiles) {
    const buf = await fs.readFile(abs)
    // Flatten — assets/index-XYZ.js → index-XYZ.js. Vite's asset filenames
    // are content-hashed, so collisions across subdirs are vanishingly
    // unlikely; if they ever do collide, fail loudly.
    const basename = rel.split('/').pop()
    if (assets[basename]) {
      console.error(`[build-dashboard] duplicate asset basename: ${basename}`)
      process.exit(1)
    }
    assets[basename] = {
      content: buf.toString('base64'),
      contentType: contentTypeFor(basename),
    }
  }

  // Emit the TypeScript module. base64 strings keep the source text-safe.
  const banner = [
    '// AUTO-GENERATED by scripts/build-dashboard.mjs — do not edit.',
    '// This file is a build artifact and gitignored.',
    '',
    'export interface DashboardAsset {',
    '  /** base64-encoded file contents. Decode at response time. */',
    '  content: string',
    '  contentType: string',
    '}',
    '',
  ].join('\n')

  const indexLiteral = JSON.stringify(indexHtml)
  const assetsLiteral = JSON.stringify(assets, null, 2)

  const body = [
    banner,
    `export const DASHBOARD_INDEX_HTML: string = ${indexLiteral}`,
    '',
    `export const DASHBOARD_LOGIN_HTML: string = DASHBOARD_INDEX_HTML`,
    '',
    `export const DASHBOARD_ASSETS: Record<string, DashboardAsset> = ${assetsLiteral}`,
    '',
  ].join('\n')

  await fs.mkdir(dirname(OUTPUT_FILE), { recursive: true })
  await fs.writeFile(OUTPUT_FILE, body, 'utf8')

  const totalBytes = Object.values(assets).reduce(
    (sum, a) => sum + Buffer.from(a.content, 'base64').byteLength,
    0,
  )
  console.log(
    `[build-dashboard] wrote ${OUTPUT_FILE} — ${Object.keys(assets).length} assets, ${(totalBytes / 1024).toFixed(1)} KiB raw`,
  )
}

main().catch((err) => {
  console.error('[build-dashboard] fatal:', err)
  process.exit(1)
})
