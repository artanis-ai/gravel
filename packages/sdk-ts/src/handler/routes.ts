/**
 * Internal HTTP route table — the dispatcher.
 *
 * The actual handlers live in per-domain files under `routes/`:
 *   - auth.ts        — me / login / logout / view-as
 *   - version.ts     — /api/version
 *   - migrations.ts  — /api/migrations/status
 *   - prompts.ts     — list / detail / submit
 *   - github.ts      — install / status / callback
 *   - samples.ts     — list / detail / feedback
 *   - shell.ts       — /, /login (dashboard SPA shell)
 *   - assets.ts      — /_assets/:id (bundled Vite assets)
 *
 * This file just merges them into one map + handles unmatched paths
 * (SPA fallthrough + 404). Same shape as the Python `_handler.py`
 * dispatcher so a host that flips between SDKs sees identical
 * behaviour.
 *
 * Spec: gravel-cloud/docs/spec/api-surface.md §5.
 */
import { json } from './index.js'
import type { RouteCtx, RouteTable } from './route-ctx.js'
import { authRoutes } from './routes/auth.js'
import { assetsRoutes } from './routes/assets.js'
import { githubRoutes } from './routes/github.js'
import { migrationsRoutes } from './routes/migrations.js'
import { promptsRoutes } from './routes/prompts.js'
import { samplesRoutes } from './routes/samples.js'
import { shellRoutes } from './routes/shell.js'
import { versionRoutes } from './routes/version.js'
import { rewriteShell } from './shell.js'
import { DASHBOARD_INDEX_HTML } from './dashboard-bundle.js'

const ROUTES: RouteTable = {
  ...authRoutes,
  ...versionRoutes,
  ...migrationsRoutes,
  ...promptsRoutes,
  ...githubRoutes,
  ...samplesRoutes,
  ...shellRoutes,
  ...assetsRoutes,
}

export async function route(ctx: RouteCtx): Promise<Response> {
  const key = `${ctx.request.method} ${matchPath(ctx.path)}`
  const handler = ROUTES[key]
  if (handler) return await handler(ctx)

  // SPA fallthrough: client-side routes (`/prompts`, `/traces/abc`,
  // etc.) 404 on hard-refresh because the dashboard is a wouter SPA
  // that owns those URLs at runtime. Serve the shell HTML for any
  // unmatched GET that's NOT under `/api/` (real API) or `/_assets/`
  // (Vite bundle). The SPA's router takes over from there.
  if (
    ctx.request.method === 'GET' &&
    !ctx.path.startsWith('/api/') &&
    !ctx.path.startsWith('/_assets/')
  ) {
    return new Response(rewriteShell(DASHBOARD_INDEX_HTML, ctx.config), {
      status: 200,
      headers: {
        'content-type': 'text/html; charset=utf-8',
        'cache-control': 'no-cache',
      },
    })
  }

  return json({ error: 'not-found', path: ctx.path }, 404)
}

function matchPath(path: string): string {
  // Normalize :id-style segments for routing. Two id shapes in play:
  //   - Prefixed ids like `p_<hex>` (prompts, datasets, etc) — `<short
  //     prefix>_<hex chars>`, generated by `manifest/hash.ts`.
  //   - UUIDs (traces, eval runs) — hex with dashes, length 8+.
  // Plus the bundled-dashboard asset route, where the trailing segment
  // is an arbitrary content-hashed Vite filename (e.g.
  // `index-D_tPEKcV.js`) that matches neither id shape — collapse it
  // to `:id` explicitly.
  let p = path
    .replace(/\/[a-z]{1,4}_[a-f0-9]{6,}(\/|$)/g, '/:id$1')
    .replace(/\/[a-f0-9-]{8,}(\/|$)/g, '/:id$1')
  p = p.replace(/^(\/_assets)\/[^/]+$/, '$1/:id')
  return p.replace(/\/$/, '') || '/'
}
